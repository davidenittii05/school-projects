
public class Cerchio {
	public double raggio,valore;
	
	public Cerchio() {
		raggio=2.3;
		valore=20;
	}
	
	public Cerchio(double raggio, double valore) {
		this.raggio=raggio;
		this.valore=valore;
	}
	
	public double getRaggio() {
		return raggio;
	}
	public void setRaggio(double raggio) {
		this.raggio=raggio;
	}
	
	public double getValore() {
		return valore;
	}
	
	public void setValore() {
		this.valore=valore;
	}
	
	
}
